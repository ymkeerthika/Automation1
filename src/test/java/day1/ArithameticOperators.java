package day1;

public class ArithameticOperators {

    public static void main(String[] args) {

        int i = 10;
        int j = 20;

        int sum = i+j;
        int sub = j-i;
        int div = j/i;
        int mul = i*j;
        int rem = i%j;
        System.out.println("Addition "+sum);
        System.out.println("SUbstration "+sub);

        System.out.println("Division "+ div);
        System.out.println("Multiplication "+ mul );
        System.out.println("Reminder "+ rem );


        int z = 10;
        z++;// Increment
        System.out.println("Increment " +z);


        int x= 10;
        x--;// Decrement
        System.out.println("Decrement "+x);

    }
}
