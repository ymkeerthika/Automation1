package day8;

public class EncapExample {
    // Varibles
    private String name;
    private long cardNumber;
    private int cvv;
    private int exp;

// Getters and setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getcardNumber() {
        return cardNumber;
    }

    public void setcardNumber(long cardName) {
        this.cardNumber = cardName;
    }

    public int getCvv() {
        return cvv;
    }

    public void setCvv(int cvv) {
        this.cvv = cvv;
    }

    public int getExp() {
        return exp;
    }

    public void setExp(int exp) {
        this.exp = exp;
    }


}
